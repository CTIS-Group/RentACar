    private void vehicleDisplayBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vehicleDisplayBTActionPerformed
        // TODO add your handling code here:
        boolean checkAvailibility = vehicleAvailableRB.isSelected();
        String result = "";
        if(vehicleCarsCB.isSelected())
            result += VehicleSys.getCarsInfo(checkAvailibility);
        if(vehicleMotorsCB.isSelected())
            result += VehicleSys.getMotorsInfo(checkAvailibility);
        if(vehicleMinivansCB.isSelected())
            result += VehicleSys.getMinivansInfo(checkAvailibility);
        if(result.length() != 0)
            vehicleDisplayTA.setText(result);
        else
            vehicleDisplayTA.setText("NO result found!");
    }//GEN-LAST:event_vehicleDisplayBTActionPerformed

