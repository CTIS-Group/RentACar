    private void fillRentedVehiclePlates()
    {
        ArrayList<Vehicle> vehicles = VehicleSys.getVehicles();
        for(int i = 0; i < vehicles.size(); i++)
            if(vehicles.get(i).isRented())
                vehiclePlateCB.addItem(vehicles.get(i).getLicencePlate());
    }
    
    public void addPlateToRentedVehicles(String plate)
    {
        vehiclePlateCB.addItem(plate);
    }
    
    private void fillCustomerIds()
    {
        ArrayList<Customer> customers = CustomerSys.getCustomers();
        for(int i = 0; i < customers.size(); i++)
            custIdCB.addItem(customers.get(i).getSsn());
    }
    
    public void addCustIdToCB(String ssn)
    {
        custIdCB.addItem( ssn );
    }
    
    private void fillEmployeeIds()
    {
        ArrayList<SalesRepresentative> salesReps = SalesRepSys.getSalesReps();
        for(int i = 0; i < salesReps.size(); i++)
            empIdCB.addItem(salesReps.get(i).getSsn());
    }
    public void addEmpIdToCB(String ssn)
    {
        empIdCB.addItem( ssn );
    }
    
    private void fillRentIds()
    {
        ArrayList<Rent> rents = RentSys.getRents();
        for(int i = 0; i < rents.size(); i++)
            rentIdCB.addItem(rents.get(i).getId() + "");
    }
    public void addRentIdToCB(int id)
    {
        rentIdCB.addItem(id + "");
    }
