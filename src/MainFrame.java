    private void empSearchBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_empSearchBTActionPerformed
        // TODO add your handling code here:
        SalesRepresentative salesRep = SalesRepSys.searchSalesRep(empIdCB.getSelectedItem().toString());
        if(salesRep == null)
            empResultTA.setText("Employee NOT FOUND");
        else
        {
            if(empMonthCB.getSelectedItem().toString().equalsIgnoreCase("none"))
                empResultTA.setText(salesRep.toString());
            else
            {
                int selectedMonth = Integer.parseInt(empMonthCB.getSelectedItem().toString());
                double salary = salesRep.getCommissionOfMonth(selectedMonth);
                empResultTA.setText(salesRep.toString() + "\nCommission in the month " + selectedMonth + " : " + salary);
            }
                
        }
    }//GEN-LAST:event_empSearchBTActionPerformed

